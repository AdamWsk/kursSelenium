public class instrukcjaWarunkowaElseIf {
    public static void main(String[] args) {
        int a = -1;

        if (a == 0) {
            System.out.println("Liczba jest równa 0");
        } else if (a>0) {
            System.out.println("Liczba jest większa od 0");
        } else if (a<0) {
            System.out.println("Liczba jest mniejsza od 0");
        }
    }
}
